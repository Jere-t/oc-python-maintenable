# Setup env (https://openclassrooms.com/fr/courses/6951236-mettez-en-place-votre-environnement-python)
# creer environement env (env est le nom qu'on donne par convention mais peut-être modifié) : 'python -m venv env'
# dans le repertoire : 'source env/bin/activate'
# desactiver l'env : 'deactivate'

# voir les modules de cette env : 'pip freeze'
# voir info sur package : 'pip show requests'

# Creer le fichier requirements.txt : 'pip freeze > requirements.txt'
# Quand on arrive dans un nouveau projet : 'python -m venv env && source env/bin/activate && pip install -r requirements.txt'
# 
# Supprimer environement virtuel : 'rm -r env/'


