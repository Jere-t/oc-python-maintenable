# Desing Patterns MVC
# C'est un pattern d’architecture, qui à la différence des design patterns, 
# s’applique sur tout votre code

# # Modèle : Le modèle contient les informations relatives à l’état du système.
#   Ce sont les fonctionnalités brutes de l’application.

# # Vue : La vue présente les informations du modèle à l’utilisateur. 
#   Elle sert d’interface visuelle et/ou sonore pour l’utilisateur.

# # Contrôleur : Le contrôleur garantit que les commandes utilisateurs soient
#   exécutées correctement, modifiant les objets du modèle appropriés, et 
#   mettant à jour l’application. C’est finalement les rouages de 
#   l’application, et c’est la couche qui apporte une interaction avec 
#   l’utilisateur.

# Un exemple se trouve dans le dossier ./mvc